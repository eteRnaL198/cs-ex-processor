#include <stdio.h>

int* insertionSort(int* arr) {
  int i, j, temp;
  for (i = 1; i < 100; i++) {
    temp = arr[i];
    j = i - 1;
    while (j >= 0 && arr[j] > temp) {
      arr[j + 1] = arr[j];
      j = j - 1;
    }
    arr[j + 1] = temp;

  }
  return arr;
}

